package j_03;

public class TypeTester {

    void printType(byte a){
        System.out.println(a + " is an byte");
    }

    void printType(int a){
        System.out.println(a + " is an int");
    }

    void printType(float a){
        System.out.println(a + " is an float");
    }

    void printType(double a){
        System.out.println(a + " is an double");
    }

    void printType(char a){
        System.out.println(a + " is an char");
    }
}
